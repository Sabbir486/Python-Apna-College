# Operations On Strings

# Concatenation

st1 = "Hello"
st2 = "World"
print(st1+st2)

# Length Of String

st3 = "HelloSabbir"
print(len(st3))

# Indexing

st4 = "King of Jungle"
print(st4[0])      # First character
print(st4[-1])     # Last character
print(st4[5:10])    # Characters from index 5 to 9 (Slicing)
print()

# Negative Indexing

print(st4[-4 : -1])