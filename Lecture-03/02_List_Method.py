list = [2,1,3]

list.append(5) #Add element at last
print(list)

list.sort() #Sort in ascending order
print(list)

list.sort(reverse=True) #Sort in descending order
print(list)

list.reverse() #Reverse the main List
print(list)

list.insert(2, 10)
print(list)

list.remove(2) #Remove first occurrence of elements
print(list)

list.pop(1) #Remove element at Index
print(list)